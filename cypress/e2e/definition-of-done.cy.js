// DOD - Definition of Done

// Estamos quase chegando ao fim dos nossos testes e quase prontos para lançar finalmente o AluraPic para o mundo.

// Mas que outras perguntas precisamos fazer antes disso? O que precisamos para fazer esse lançamento?

// Precisamos, por exemplo, de um pré-lançamento para alguns usuários Beta que se inscreveram ou para algumas pessoas a mais testarem antes de colocarmos para o mundo inteiro? Sim, podemos definir que sim.

// Nós testamos esse sistema, e precisamos definir: ele pode apresentar bugs? Por exemplo, podemos lançá-lo com algum bug menor, alguma coisa que seja mais visual, mas que não impacte a funcionalidade? Podemos definir que sim ou que não.

// Nesse caso eu vou colocar que não. Esse sistema não pode apresentar bugs, pelo menos não bugs tão graves.

// Precisamos passar por alguma outra validação além do QA? Por exemplo, passar para algum time de segurança que vai garantir que o nosso login é seguro; ou de repente alguma atividade além de quando lançarmos, como um time de marketing que vai fazer um grande comercial sobre o AluraPic?

// Precisamos saber e fazer algum teste para ver quantos usuários aguentamos no nosso sistema? Ou seja, algum outro tipo de validação, além da nossa validação precisa ser feita?

// Isso tudo que eu falei é o Definition of Done, que é a definição de pronto, conhecido popularmente como DOD, porque vem dessa sigla em inglês.

// Ele define se uma atividade foi concluída ou não, de acordo com os critérios que estabelecemos. Ele tem que valer para todas as funcionalidades, então tem que ser uma coisa genérica, não pode ser como os critérios de aceite, que têm uma grande diferença.

// O critério de aceite eu faço para cada funcionalidade. Eu tenho os critérios de aceite para a funcionalidade de login do AluraPic, que definimos anteriormente. E eu tenho, por exemplo, outros critérios de aceite para uma funcionalidade de cadastro.

// Mas a definição de pronto tem que ser para tudo, para todo o nosso sistema. O que consideramos como pronto para todas as nossas atividades?

// Essa definição de pronto é utilizada por vários times ágeis, então pode ser revisada e atualizada conforme o sistema for crescendo.

// Por exemplo, um sistema pequeno pode ter uma definição de pronto diferente de quando o sistema já está com várias funcionalidades. É sempre importante revisar e atualizar. E não esquecer de que isso tem que ser feito para o sistema como um todo.

// Pode parecer um pouco óbvio termos que definir o que é o pronto e porque o sistema está pronto para ser lançado, mas isso garante que todo mundo do time saiba quando o sistema está pronto. Ele serve para alinhar as expectativas e é por isso que é sempre bom manter isso revisado e atualizado.

// E agora que definimos, e se todos esses critérios de prontos forem atingidos, estamos prontos para lançar o sistema.



//Qualidade em metodologias ágeis

// Já sabemos que a pessoa responsável pela QA (quality assurance) tem um papel importante na garantia de que um software
//  está sendo desenvolvido com qualidade.
//  O trabalho como QA também depende de qual é a metodologia utilizada no desenvolvimento do produto.
//  O papel de QA em metodologias ágeis Participar de todo o processo de produção, visando prever defeitos e melhorar a qualidade do produto.
//  Ser um disseminador das metodologias de testes e ferramentas e cultura de qualidade.

//  Em times ágeis o QA participa de todo o processo e, quanto antes achar o erro, mais barato para o time corrigir.


